// 基础图形绘制示例
m.background(240, 240, 240); // 设置背景色

// 矩形
m.fill(255, 0, 0);
m.rect(100, 100, 100, 100);

// 椭圆
m.fill(0, 255, 0);
m.ellipse(300, 100, 100, 100);

// 直线
m.stroke(0, 0, 255);
m.strokeWeight(3);
m.line(100, 250, 300, 250);

// 三角形
m.fill(255, 255, 0);
m.triangle(100, 300, 200, 400, 300, 300);

// 添加标签
m.fill(0);
m.textSize(14);
m.text("矩形", 130, 150);
m.text("椭圆", 280, 100);
m.text("直线", 190, 240);
m.text("三角形", 180, 350);
